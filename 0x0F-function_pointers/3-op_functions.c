#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - functions that adds
 * @a: argument 1
 * @b: argument 2
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - functions that subtracts
 * @a: argument 1
 * @b: argument 2
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - functions that multiplies
 * @a: argument 1
 * @b: argument 2
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - functions that divides
 * @a: argument 1
 * @b: argument 2
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - functions that gets remainder
 * @a: argument 1
 * @b: argument 2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
